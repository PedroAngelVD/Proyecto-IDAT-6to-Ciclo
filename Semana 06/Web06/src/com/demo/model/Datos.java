package com.demo.model;

public class Datos {
	private int alumM;
	private int alumF;
	
	public Datos() {
		super();
	}
	
	public Datos(int alumM, int alumF) {
		super();
		this.alumM = alumM;
		this.alumF = alumF;
	}
	
	public int getAlumM() {
		return alumM;
	}
	public void setAlumM(int alumM) {
		this.alumM = alumM;
	}
	public int getAlumF() {
		return alumF;
	}
	public void setAlumF(int alumF) {
		this.alumF = alumF;
	}
	
}
