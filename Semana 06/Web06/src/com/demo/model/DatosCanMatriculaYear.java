package com.demo.model;

public class DatosCanMatriculaYear {

	private int f2015M;
	private int f2016M;
	private int f2017M;
	private int f2018M;
	private int f2019M;
	private int f2020M;
	
	public DatosCanMatriculaYear() {
		super();
	}
	
	public DatosCanMatriculaYear(int f2015m, int f2016m, int f2017m, int f2018m, int f2019m, int f2020m) {
		super();
		f2015M = f2015m;
		f2016M = f2016m;
		f2017M = f2017m;
		f2018M = f2018m;
		f2019M = f2019m;
		f2020M = f2020m;
	}
	
	public int getF2015M() {
		return f2015M;
	}
	public void setF2015M(int f2015m) {
		f2015M = f2015m;
	}
	public int getF2016M() {
		return f2016M;
	}
	public void setF2016M(int f2016m) {
		f2016M = f2016m;
	}
	public int getF2017M() {
		return f2017M;
	}
	public void setF2017M(int f2017m) {
		f2017M = f2017m;
	}
	public int getF2018M() {
		return f2018M;
	}
	public void setF2018M(int f2018m) {
		f2018M = f2018m;
	}
	public int getF2019M() {
		return f2019M;
	}
	public void setF2019M(int f2019m) {
		f2019M = f2019m;
	}
	public int getF2020M() {
		return f2020M;
	}
	public void setF2020M(int f2020m) {
		f2020M = f2020m;
	}
	
}
