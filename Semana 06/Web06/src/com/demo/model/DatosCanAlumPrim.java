package com.demo.model;

public class DatosCanAlumPrim {

	private int PrimeroPrim;
	private int SegundoPrim;
	private int TerceroPrim;
	private int CuartoPrim;
	private int QuintoPrim;
	private int SextoPrim;
	
	public DatosCanAlumPrim() {
		super();
	}
	
	public DatosCanAlumPrim(int primeroPrim, int segundoPrim, int terceroPrim, int cuartoPrim, int quintoPrim,
			int sextoPrim) {
		super();
		PrimeroPrim = primeroPrim;
		SegundoPrim = segundoPrim;
		TerceroPrim = terceroPrim;
		CuartoPrim = cuartoPrim;
		QuintoPrim = quintoPrim;
		SextoPrim = sextoPrim;
	}

	public int getPrimeroPrim() {
		return PrimeroPrim;
	}

	public void setPrimeroPrim(int primeroPrim) {
		PrimeroPrim = primeroPrim;
	}

	public int getSegundoPrim() {
		return SegundoPrim;
	}

	public void setSegundoPrim(int segundoPrim) {
		SegundoPrim = segundoPrim;
	}

	public int getTerceroPrim() {
		return TerceroPrim;
	}

	public void setTerceroPrim(int terceroPrim) {
		TerceroPrim = terceroPrim;
	}

	public int getCuartoPrim() {
		return CuartoPrim;
	}

	public void setCuartoPrim(int cuartoPrim) {
		CuartoPrim = cuartoPrim;
	}

	public int getQuintoPrim() {
		return QuintoPrim;
	}

	public void setQuintoPrim(int quintoPrim) {
		QuintoPrim = quintoPrim;
	}

	public int getSextoPrim() {
		return SextoPrim;
	}

	public void setSextoPrim(int sextoPrim) {
		SextoPrim = sextoPrim;
	}
	
}
